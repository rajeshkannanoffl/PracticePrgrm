#!/usr/bin/perl
# next-line \n
print "Hello World\n";
print "-------------\n";

print "TabSpace01\n";
# \t gives tab space
print "\t";
print "TabSpace02\n";
print "-------------\n";

# \u \U - for uppercase
print "\Urajesh\n";
print "\urajesh\n";
print "-------------\n";

# \l \L for lowercase
print "\LRAJESH\n";
print "\lRAJESH\n";
print "-------------\n";

# Combination of Up & Lw
print "Rajesh \Ukannan\E\n";
